// Portfolio controller — محفظة أعمال المقاول
const { z } = require('zod');
const PortfolioItem = require('../models/PortfolioItem');
const { AppError, asyncHandler } = require('../middleware/errorHandler');

// GET /api/portfolio/:contractorId — عام
const getPortfolio = asyncHandler(async (req, res) => {
  const items = await PortfolioItem.find({ contractor: req.params.contractorId })
    .sort({ createdAt: -1 })
    .lean();
  res.json({ items, total: items.length });
});

// POST /api/portfolio — المقاول فقط
const addPortfolioItem = asyncHandler(async (req, res) => {
  const { title, description, projectType, completedAt, governorate, durationMonths } = req.body;
  if (!title || title.length < 3) throw new AppError('العنوان مطلوب', 400, 'VALIDATION_ERROR');

  const images = [];
  if (req.files) {
    const fileList = req.files.images || req.files.beforePhotos || [];
    fileList.forEach((f) => {
      images.push({ filename: f.filename, originalName: f.originalname, mimetype: f.mimetype, size: f.size });
    });
  }

  const beforePhotos = (req.files?.beforePhotos || []).map((f) => ({
    filename: f.filename, originalName: f.originalname, mimetype: f.mimetype, size: f.size,
  }));

  const afterPhotos = (req.files?.afterPhotos || []).map((f) => ({
    filename: f.filename, originalName: f.originalname, mimetype: f.mimetype, size: f.size,
  }));

  const item = await PortfolioItem.create({
    contractor: req.user._id,
    title,
    description: description || '',
    projectType: projectType || 'other',
    images,
    beforePhotos,
    afterPhotos,
    completedAt: completedAt ? new Date(completedAt) : new Date(),
    governorate: governorate || '',
    durationMonths: durationMonths !== undefined && durationMonths !== '' ? Number(durationMonths) : null,
    isAutoGenerated: false,
  });

  res.status(201).json({ item });
});

// DELETE /api/portfolio/:id — المقاول فقط (مشاريع يدوية)
const deletePortfolioItem = asyncHandler(async (req, res) => {
  const item = await PortfolioItem.findById(req.params.id);
  if (!item) throw new AppError('العنصر غير موجود', 404, 'NOT_FOUND');
  if (item.contractor.toString() !== req.user._id.toString()) {
    throw new AppError('غير مصرح', 403, 'FORBIDDEN');
  }
  if (item.isAutoGenerated) {
    throw new AppError('لا يمكن حذف عناصر محفظة تم إنشاؤها تلقائياً من المنصة', 400, 'AUTO_GENERATED');
  }
  await item.deleteOne();
  res.json({ ok: true, message: 'تم حذف العنصر بنجاح' });
});

module.exports = { getPortfolio, addPortfolioItem, deletePortfolioItem };
